import java.util.HashSet;

public class Hashing {

	public static void main(String[] args) {
		HashSet <String> hs = new HashSet <String> ();
		
        hs.add("java");
        hs.add("programming");
        hs.add("is");
        hs.add("hard");
        hs.add("to");
        hs.add("learn");
        
        System.out.println(hs);
        
	}

}
